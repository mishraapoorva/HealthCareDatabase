    private void populateTable() throws SQLException {
        DefaultTableModel model = (DefaultTableModel) appointmentTable.getModel();
        model.setRowCount(0);
        String getAppointments = "select appointment_id,last_name+','+' '+first_name as 'Patient Name',room_number,start_time,end_time,status from appointment\n"
                + "join patient on appointment.patient_id = patient.patient_id where dentist_id = '" + doctor_id + "'";
        ResultSet rs = statement.executeQuery(getAppointments);
        while (rs.next()) {
            model.addRow(new Object[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4) + rs.getString(5), rs.getString(6)});
        }
    }
    private void populateFields() {
        try {

            statement = connection.createStatement();
            String doctorPhoto = "select doctor_Photo from doctor where doctor_id = '" + doctor_id + "'";
            String doctorDetails = "select first_name,middle_name,last_name,contact_number from doctor where doctor_id ='" + doctor_id + "'";
            String licenseDetails = "select license_number,authority,expiry_date from doctor_license where doctor_id ='" + doctor_id + "'";
            ResultSet rs = statement.executeQuery(doctorPhoto);
            while (rs.next()) {
                BufferedImage im = ImageIO.read(rs.getBinaryStream("doctor_Photo"));
                Image dimg = im.getScaledInstance(pictureLabel.getWidth(), pictureLabel.getHeight(),
                        Image.SCALE_SMOOTH);
                pictureLabel.setIcon(new ImageIcon(dimg));
            }
            ResultSet rs1 = statement.executeQuery(doctorDetails);
            while (rs1.next()) {
                firstNameTextField.setText(rs1.getString(1));
                middleNameTextField.setText(rs1.getString(2));
                lastNameTextField.setText(rs1.getString(3));
                contactTextField.setText(rs1.getString(4));
            }
            ResultSet rs2 = statement.executeQuery(licenseDetails);
            while (rs2.next()) {
                licenseNumber.setText(rs2.getString(1));
                authority.setText(rs2.getString(2));
                expiryDate.setText(rs2.getString(3));
            }

        } catch (Exception err) {
            JOptionPane.showMessageDialog(this, err.getMessage());
        }
    }

    public ImageIcon ResizeImage(String ImagePath) {
        ImageIcon MyImage = new ImageIcon(ImagePath);
        Image img = MyImage.getImage();

        Image newImg = img.getScaledInstance(pictureLabel.getWidth(), pictureLabel.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImg);
        return image;
    }

