    private void proceedButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proceedButtonActionPerformed
        // TODO add your handling code here:

        int selectedRow = appointmentTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row to Process", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String appointment = appointmentTable.getValueAt(selectedRow, 0).toString();

        TreatmentPanel treatment = null;
        try {
            treatment = new TreatmentPanel(statement, userProcessContainer, connection, appointment);
        } catch (SQLException ex) {
            Logger.getLogger(DoctorPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        userProcessContainer.add("DoctorPanel", treatment);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_proceedButtonActionPerformed

    private void cancelAppointmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelAppointmentActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = appointmentTable.getSelectedRow();
        String appointmentId = appointmentTable.getValueAt(selectedRow, 0).toString();
        String cancelAppointment = "UPDATE [dbo].[appointment]\n"
                + "   SET \n"
                + "      [status] = 'Cancelled'\n"
                + " WHERE appointment_id = '" + appointmentId + "'";
        
        try {
            statement.executeUpdate(cancelAppointment);
            JOptionPane.showMessageDialog(null,"Appointment Cancelled");
            populateTable();
        } catch (SQLException ex) {
            Logger.getLogger(DoctorPanel.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_cancelAppointmentActionPerformed

