    private void saveEquipButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveEquipButtonActionPerformed
        // TODO add your handling code here:
        
        String equipName = equipmentName.getText();
        String equipId = equipmentId.getText();
        DateFormat sysDate = new SimpleDateFormat("yyyy-MM-dd hh:mm:ss");
        String maintainance = sysDate.format(mainatainanceDatePicker.getDate());
        String warranty = sysDate.format(warrantyDatePicker.getDate());
        String purchase = sysDate.format(purchaseDatePicker.getDate());
        String roomNumber = roomComboBox.getSelectedItem().toString();
           String createEquipment = "INSERT INTO [dbo].[equipment]\n"
                + "           ([equipment_name]\n"
                + "           ,[maintenance_due_date]\n"
                + "           ,[warranty_end_date]\n"
                + "           ,[purchase_date]\n"
                + "           ,[equipment_id]\n"
                + "           ,[room_number])\n"
                + "     VALUES\n"
                + "           ('" + equipName + "','" + maintainance + "','" + warranty + "','" + purchase + "','" + equipId + "','" + roomNumber + "')";
           
        try {
            statement.executeUpdate(createEquipment);
            JOptionPane.showMessageDialog(null, "Equipment Added Successfully");
            populateEquipmentTable();
        addEquipmentButton.setEnabled(true);
        equipmentName.setEnabled(false);
        mainatainanceDatePicker.setEnabled(false);
        warrantyDatePicker.setEnabled(false);
        purchaseDatePicker.setEnabled(false);
        equipmentId.setEnabled(false);
        saveEquipButton.setEnabled(false);
        roomComboBox.setEnabled(false);
        } catch (SQLException ex) {
            Logger.getLogger(EquipmentsPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }//GEN-LAST:event_saveEquipButtonActionPerformed

