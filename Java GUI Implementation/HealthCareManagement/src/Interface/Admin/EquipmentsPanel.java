    private void populateSuppliesTable() throws SQLException {
        DefaultTableModel model = (DefaultTableModel) suppliesTable.getModel();

        CallableStatement stmt = null;
        //String getTreatments = "  select * from [Supplies Availability]";
        stmt = connection.prepareCall("select * from [Supplies]");
        ResultSet rs = stmt.executeQuery();
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString(1),
                rs.getString(2),});
        }
    }
private void populateEquipmentTable() throws SQLException{
    
DefaultTableModel model = (DefaultTableModel) equipmentTable.getModel();
        model.setRowCount(0);
         
        String getTreatments = "  select * from equipment";
        ResultSet rs = statement.executeQuery(getTreatments);
        while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString(1), 
                    rs.getString(2),
                     rs.getString(3),
                      rs.getString(4),
                       rs.getString(5),
                });
            }
}
   
