    private void populateDefault() throws SQLException {

        locationComboBox.removeAllItems();
        doctorComboBox.removeAllItems();
        roomComboBox.removeAllItems();
        patientComboBox.removeAllItems();

        String getLocation = "select addressline1 , location_id from location";
        ResultSet location_result = statement.executeQuery(getLocation);

        while (location_result.next()) {
            locationComboBox.addItem(location_result.getString(2).trim() + " - " + location_result.getString(1).trim());
        }

        String getDoctor = "select doctor_id , first_name , last_name from doctor";
        ResultSet doctor_result = statement.executeQuery(getDoctor);

        while (doctor_result.next()) {

            doctorComboBox.addItem(doctor_result.getString(1).trim() + " - "
                    + doctor_result.getString(2).trim() + " "
                    + doctor_result.getString(3).trim());
        }

    }
     
  
