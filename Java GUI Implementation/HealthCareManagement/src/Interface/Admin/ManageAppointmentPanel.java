    private void bookAppointmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookAppointmentActionPerformed
        // TODO add your handling code here:
        
           try {
            bookAppointment.setEnabled(false);
            CallableStatement stmt = null;
            String patient = patientComboBox.getSelectedItem().toString();
            String patientID = patient.substring(0, patient.indexOf("-")).trim();

            String doctor = doctorComboBox.getSelectedItem().toString();
            String doctorID = doctor.substring(0, doctor.indexOf("-")).trim();

            String roomNumber = roomComboBox.getSelectedItem().toString();

            DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String date = dateFormat.format(jXDatePicker2.getDate());

            String status ="Booked";

            String time = timeComboBox.getSelectedItem().toString();

            String startTime = time.substring(0, time.indexOf("-")).trim() + ":00";
            String endTime = time.substring(time.indexOf("-")+1).trim() + ":00";

            System.out.println(startTime);
            System.out.println(endTime);

            stmt = connection.prepareCall("{call insertAppointmentRow(?,?,?,?,?,?,?)}");

            stmt.setString(1, patientID);
            stmt.setString(2, status);
            stmt.setString(3, roomNumber);
            stmt.setString(4, date);
            stmt.setString(5, startTime);
            stmt.setString(6, endTime);
            stmt.setString(7, doctorID);

            stmt.executeUpdate();

            JOptionPane.showMessageDialog(null, "Appointment Booked successfully");

            String getEmailID = "select email_address from patient where patient_id = '" + patientID +"'";

            ResultSet email_result = statement.executeQuery(getEmailID);
            String patient_email ="";

            while (email_result.next()) {

                patient_email =      email_result.getString(1).trim();
            }

            CallableStatement stmtEmail = connection.prepareCall("{call send_mail_db(?,?,?,?)}");
            stmtEmail.setString(1, "Wecare_Hospital");
            stmtEmail.setString(2, patient_email);
            stmtEmail.setString(3, TextArea.getText());
            stmtEmail.setString(4, "Appointment Booking with Wecare Hospital");

            stmtEmail.executeUpdate();

            JOptionPane.showMessageDialog(null, "Email Sent successfully");
             verifyButton.setEnabled(true);
        } catch (SQLException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_bookAppointmentActionPerformed

